package main

import "fmt"

func main() {
	var inteiro int = 12
	var flutuante float64 = 3.14
	var booleano bool = true
	var string string = "Paulo Henrique"

	fmt.Printf(" Inteiro: %d \n", inteiro)
	fmt.Printf(" Flutuante: %.2f \n", flutuante)
	fmt.Printf(" Booleano: %v \n", booleano)
	fmt.Printf(" String: %v \n", string)

}
